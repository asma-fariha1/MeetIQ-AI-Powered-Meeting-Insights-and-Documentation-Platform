# ==========================================
# Custom Canvas for Dynamic Page Numbering
# ==========================================
class NumberedCanvas(canvas.Canvas):
    """
    Two-pass canvas renderer to dynamically calculate total pages 
    and draw consistent running headers and footers.
    """
    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)
        self._saved_page_states = []

    def showPage(self):
        # Save state for the second rendering pass
        self._saved_page_states.append(dict(self.__dict__))
        self._startPage()

    def save(self):
        # Second pass: draw headers/footers with correct total page count
        page_count = len(self._saved_page_states)
        for state in self._saved_page_states:
            self.__dict__.update(state)
            self.draw_page_decorations(page_count)
            super().showPage()
        super().save()

    def draw_page_decorations(self, page_count):
        self.saveState()
        self.setFont("Helvetica", 8)
        self.setFillColor(colors.HexColor("#4B5563")) # Dark Gray
        
        # 1. Top Running Header (draw on page 2 and later)
        if self._pageNumber > 1:
            self.drawString(54, 750, "Minutes of Meeting (MoM)")
            self.setStrokeColor(colors.HexColor("#E5E7EB")) # Light Gray line
            self.setLineWidth(0.5)
            self.line(54, 742, 558, 742)
            
        # 2. Bottom Running Footer (draw on all pages)
        generation_time = datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        footer_text = f"Generated by AI Meeting Assistant | Powered by Groq + Whisper + ElevenLabs | Date: {generation_time}"
        self.drawString(54, 40, footer_text)
        
        # Draw Right Aligned "Page X of Y"
        page_num_str = f"Page {self._pageNumber} of {page_count}"
        self.drawRightString(558, 40, page_num_str)
        
        self.setStrokeColor(colors.HexColor("#E5E7EB"))
        self.setLineWidth(0.5)
        self.line(54, 52, 558, 52)
        
        self.restoreState()

